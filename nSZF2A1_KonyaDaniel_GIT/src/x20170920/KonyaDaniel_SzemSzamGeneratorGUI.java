    private File letrehozFajl() {
        String eleresiUt = System.getProperty("user.home") + "/Documents/generalt/";
        File file = new File(eleresiUt+"generalt.txt");
        try {
            if (file.exists()) {
                file = new File(eleresiUt);
                for (File fileok : file.listFiles()) {
                    fileok.delete();
                }
                file.delete();
                file = new File(eleresiUt);
                file.mkdirs();
                file = new File(eleresiUt + "generalt.txt");
                file.createNewFile();
            } else {
                file = new File(eleresiUt);
                file.mkdirs();
                file = new File(eleresiUt + "generalt.txt");
                file.createNewFile();
            }
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        return file;
    }
