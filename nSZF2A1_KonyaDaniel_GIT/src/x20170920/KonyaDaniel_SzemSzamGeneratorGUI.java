    private void btnEllenorzesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEllenorzesMouseClicked
        String szemSzam = tfSzemSzamEll.getText();
        int szulEv;

        lbEredmeny.setFont(new Font("Dialog", Font.PLAIN, 18));

        if (szemSzam.length() > 11 || szemSzam.length() < 11) {
            JOptionPane.showMessageDialog(rootPane, "Kérem, csak 11 szám hosszúságú személyi számot adjon meg!", "Hosszúsági hiba", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                szulEv = Integer.parseInt(szemSzam.substring(1, 3));

                if (szulEv < 97) {
                    RegiAlgoritmus(szemSzam);
                } else {
                    UjAlgoritmus(szemSzam);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Kérem, csak számokat adjon meg!", "Formai hiba", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEllenorzesMouseClicked

    void fajlbaKiir(String szemszam, File file) {
        PrintWriter pw = null;
        try {
            pw = new PrintWriter(new FileOutputStream(file, true));
            pw.println(szemszam);
            
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } finally {
            pw.close();
        }
    }

    void RegiAlgoritmus(String szemSzam) {
        int ellenorzendoSzam = Integer.parseInt(szemSzam.substring(szemSzam.length() - 1));
        int ellenorzoSzam = 0;
        int nem = Integer.parseInt(szemSzam.substring(0, 1));

        for (int i = 0; i < szemSzam.length(); i++) {

            int k = Integer.parseInt(szemSzam.substring(i, i + 1));

            ellenorzoSzam += k * (i + 1);
        }

        if (ellenorzoSzam % 11 != ellenorzendoSzam) {
            lbEredmeny.setText("<html><font style='color:#DC3545'>A megadott személyi szám érvénytelen!</font></html>");
        } else {
            lbEredmeny.setText("<html>A megadott személyi szám alapján a személy neme: " + (nem % 2 == 0 ? "<font style='color:#F82EF6'>Nő</font>" : "<font style='color:#3A8EE8'>Férfi</font>") + "<br> <font style='color:#5CB85C'>Érvényes!</font></html>");
        }
    }

    void UjAlgoritmus(String szemSzam) {
        int ellenorzendoSzam = Integer.parseInt(szemSzam.substring(szemSzam.length() - 1));
        int ellenorzoSzam = 0;
        int nem = Integer.parseInt(szemSzam.substring(0, 1));

        int j = 0;

        for (int i = szemSzam.length() - 1; i >= 0; i--) {

            int k = Integer.parseInt(szemSzam.substring(j, j + 1));

            ellenorzoSzam += k * i;

            j++;
        }

        if (ellenorzoSzam % 11 != ellenorzendoSzam) {
            lbEredmeny.setText("<html><font style='color:#DC3545'>A megadott személyi szám érvénytelen!</font></html>");
        } else {
            lbEredmeny.setText("<html>A megadott személyi szám alapján a személy neme: " + (nem % 2 == 0 ? "<font style='color:#F82EF6'>Nő</font>" : "<font style='color:#3A8EE8'>Férfi</font>") + "<br> <font style='color:#5CB85C'>Érvényes!</font></html>");
        }
    }

